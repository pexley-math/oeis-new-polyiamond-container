# ============================================================
# Triangular grid adjacency
# ============================================================

def get_neighbours(r, c):
    """Return 3 edge-neighbors of triangle (r, c).

    Up-pointing (r+c even): shares edges left, right, and above.
    Down-pointing (r+c odd): shares edges left, right, and below.
    """
    if (r + c) % 2 == 0:
        return ((r, c - 1), (r, c + 1), (r - 1, c))
    else:
        return ((r, c - 1), (r, c + 1), (r + 1, c))


# ============================================================
# Triangular coordinate transforms (D6: 12 symmetries)
# ============================================================

def normalize(cells):
    """Translate to canonical position using valid lattice translations.

    The triangular grid's translation lattice is generated by (0, 2) and
    (1, 1). Only translations with even (dr + dc) sum preserve triangle
    orientations. Result has min_r = 0 and min_c in {0, 1}.
    """
    if not cells:
        return frozenset()
    min_r = min(r for r, c in cells)
    min_c = min(c for r, c in cells)
    k = (min_c - min_r) // 2
    dc = min_r + 2 * k
    return frozenset((r - min_r, c - dc) for r, c in cells)


def _rot60(r, c):
    """60-degree clockwise rotation around vertex (0,0)."""
    t = (r + c) % 2
    r2 = (r + c + 1) // 2
    c2 = (c - 3 * r - t - 2) // 2
    return (r2, c2)


def _reflect(r, c):
    """Horizontal reflection about the x-axis through vertex (0,0)."""
    return (-r - 1, c)


def _make_transform(n_rot, do_reflect):
    """Create a D6 transform: optionally reflect, then rotate n_rot times."""
    def transform(r, c):
        if do_reflect:
            r, c = _reflect(r, c)
        for _ in range(n_rot):
            r, c = _rot60(r, c)
        return (r, c)
    return transform


# All 12 D6 transforms (6 rotations + 6 reflected rotations)
TRI_TRANSFORMS = []
for _k in range(6):
    TRI_TRANSFORMS.append(_make_transform(_k, False))
for _k in range(6):
    TRI_TRANSFORMS.append(_make_transform(_k, True))


def tri_orientations(cells):
    """Return all distinct orientations (up to 12) of a polyiamond."""
    results = set()
    for t in TRI_TRANSFORMS:
        oriented = normalize(frozenset(t(r, c) for r, c in cells))
        results.add(oriented)
    return list(results)


# ============================================================
# Polyiamond generation
# ============================================================

def gen_fixed(n):
    """Generate all fixed n-iamonds (distinct under translation only)."""
    if _USE_CYTHON_ENUM:
        return _pe_fixed(n, "triangle")
    if n <= 0:
        return [frozenset()]
    if n == 1:
        return [frozenset([(0, 0)])]
    prev = gen_fixed(n - 1)
    seen = set()
    out = []
    for p in prev:
        for r, c in p:
            for nb in get_neighbours(r, c):
                if nb not in p:
                    nw = normalize(p | {nb})
                    if nw not in seen:
                        seen.add(nw)
                        out.append(nw)
    return out


def gen_free(n):
    """Generate all free n-iamonds (distinct under rotation and reflection)."""
    if _USE_CYTHON_ENUM:
        return _pe_free(n, "triangle")
    if n <= 0:
        return [frozenset()]
    fixed = gen_fixed(n)
    seen = set()
    free = []
    for p in fixed:
        canon = min(tuple(sorted(o)) for o in tri_orientations(p))
        if canon not in seen:
            seen.add(canon)
            free.append(p)
    return free


# ============================================================
# Placement enumeration
# ============================================================

def all_placements(free_polys, grid_rows, grid_cols):
    """Enumerate all valid placements of each free polyiamond in the grid.

    Grid cells: (r, c) with r in [0, grid_rows-1], c in [0, grid_cols-1].
    All orientations and all translation offsets are tried; deduplication
    via seen_sets eliminates redundant placements.
    """
    result = []
    for poly in free_polys:
        piece_placements = []
        seen_sets = set()
        for ori in tri_orientations(poly):
            cells = list(ori)
            max_r = max(r for r, c in cells)
            max_c = max(c for r, c in cells)
            min_r = min(r for r, c in cells)
            min_c = min(c for r, c in cells)
            for dr in range(-min_r, grid_rows - max_r):
                for dc in range(-min_c, grid_cols - max_c):
                    placed = tuple(sorted((r + dr, c + dc) for r, c in cells))
                    if placed not in seen_sets:
                        seen_sets.add(placed)
                        piece_placements.append(placed)
        result.append(piece_placements)
    return result


# ============================================================
# Connectivity check (triangular adjacency)
# ============================================================

def find_components(cells):
    """Find connected components of a set of triangular cells via BFS."""
    remaining = set(cells)
    components = []
    while remaining:
        start = next(iter(remaining))
        comp = set()
        queue = deque([start])
        comp.add(start)
        remaining.remove(start)
        while queue:
            r, c = queue.popleft()
            for nb in get_neighbours(r, c):
                if nb in remaining:
                    remaining.remove(nb)
                    comp.add(nb)
                    queue.append(nb)
        components.append(comp)
    return components


def is_connected(cells):
    """Return True if the given set of triangular cells is connected."""
    if len(cells) <= 1:
        return True
    return len(find_components(cells)) == 1


# ============================================================
# Solution verification
# ============================================================

def verify_solution(n, cells, free_polys, verbose=False):
    """
    Verify that a candidate solution is valid:
      1. Polyiamond is connected
      2. Every free n-iamond fits within the polyiamond
    Returns (ok, message).
    """
    cell_set = set(cells)
    size = len(cell_set)

    if not is_connected(cell_set):
        return False, "Polyiamond is not connected"

    if n <= 1:
        return True, (f"OK -- {size} cells, connected, trivially contains "
                      f"all free {n}-iamonds")

    # Compute bounding box for offset search
    min_r = min(r for r, c in cell_set)
    max_r = max(r for r, c in cell_set)
    min_c = min(c for r, c in cell_set)
    max_c = max(c for r, c in cell_set)

    for idx, poly in enumerate(free_polys):
        fits = False
        for ori in tri_orientations(poly):
            ori_cells = list(ori)
            omr = min(r for r, c in ori_cells)
            omc = min(c for r, c in ori_cells)
            norm = [(r - omr, c - omc) for r, c in ori_cells]
            p_max_r = max(r for r, c in norm)
            p_max_c = max(c for r, c in norm)
            for dr in range(min_r, max_r - p_max_r + 1):
                for dc in range(min_c, max_c - p_max_c + 1):
                    placed = set((r + dr, c + dc) for r, c in norm)
                    if placed <= cell_set:
                        fits = True
                        break
                if fits:
                    break
            if fits:
                break
        if not fits:
            return False, f"Free {n}-iamond #{idx} does not fit"
        if verbose:
            print(f"    Piece {idx + 1}/{len(free_polys)}: fits")

    return True, (f"OK -- {size} cells, connected, all {len(free_polys)} "
                  f"free {n}-iamonds fit")


# ============================================================
# SAT solver
# ============================================================

def solve_sat(n_target, grid_rows, grid_cols, upper_bound, free_polys,
              use_shape=False, verbose=False):
    """
    SAT-based top-down search for the smallest polyiamond containing all
    free n-iamonds.

    If use_shape=True, adds shape constraints:
      1. Contiguous c-values per row (no gaps within rows)
      2. At least one full row (all c values occupied, width = grid_cols = n)

    Connectivity enforced via iterative CEGAR cuts.
    """
    placements = all_placements(free_polys, grid_rows, grid_cols)
    n_pieces = len(free_polys)

    def var(r, c):
        return r * grid_cols + c + 1

    total_vars = grid_rows * grid_cols
    cell_vars = [var(r, c) for r in range(grid_rows) for c in range(grid_cols)]

    # Auxiliary variables for placements
    aux_id = total_vars + 1
    piece_aux = []
    for pplacements in placements:
        pvars = list(range(aux_id, aux_id + len(pplacements)))
        aux_id += len(pplacements)
        piece_aux.append(pvars)

    total_place = sum(len(pa) for pa in piece_aux)
    if verbose:
        print(f"    {grid_rows} X {grid_cols}: {total_vars} cell vars, "
              f"{total_place} placements, {n_pieces} pieces")

    for i in range(n_pieces):
        if not piece_aux[i]:
            if verbose:
                print(f"    Piece {i} has no valid placements -- infeasible")
            return None, None

    # ---- Shape constraints ----
    shape_clauses = []
    if use_shape:
        # 1. Contiguous c-values per row:
        #    For each row r and columns a < b < c,
        #    if x[r,a] and x[r,c] then x[r,b]
        contiguous_count = 0
        for r in range(grid_rows):
            for a in range(grid_cols):
                for c_end in range(a + 2, grid_cols):
                    for b in range(a + 1, c_end):
                        shape_clauses.append(
                            [-var(r, a), var(r, b), -var(r, c_end)])
                        contiguous_count += 1

        # 2. At least one full row (all c values occupied)
        full_row_vars = []
        for r in range(grid_rows):
            fv = aux_id
            aux_id += 1
            full_row_vars.append(fv)
            # fv -> x[r,c] for all c
            for c in range(grid_cols):
                shape_clauses.append([-fv, var(r, c)])
            # (all x[r,c]) -> fv
            shape_clauses.append([fv] + [-var(r, c) for c in range(grid_cols)])

        # At least one full row
        shape_clauses.append(full_row_vars[:])

        # No row pinning -- learned from polyhex containers that pinning
        # the full row to row 0 is WRONG on non-square grids. The optimal
        # solution may have the full row interior.

        if verbose:
            print(f"    Shape constraints: {contiguous_count} contiguous, "
                  f"{len(full_row_vars)} full-row vars")
            print(f"    Total shape clauses: {len(shape_clauses)}")

    # Two-phase search: binary search to narrow range, then linear
    # descent for proof. Problem is monotone (SAT at k => SAT at k+1)
    # so binary search is valid even with shape constraints.
    best_size = None
    best_cells = None

    lo = n_target
    hi = upper_bound

    # Phase 1: Binary search to find tight upper bound
    if hi - lo > 6:
        print(f"    Phase 1: Binary search [{lo}..{hi}]")
        while hi - lo > 4:
            mid = (lo + hi) // 2
            t1 = time.time()
            result = _try_solve(grid_rows, grid_cols, mid, placements,
                                piece_aux, n_pieces, var, cell_vars,
                                shape_clauses, aux_id)
            elapsed = time.time() - t1

            if result is not None:
                best_size = mid
                best_cells = result
                hi = mid
                print(f"    k = {mid}: SAT  [{elapsed:.1f}s]")
            else:
                lo = mid + 1
                print(f"    k = {mid}: UNSAT  [{elapsed:.1f}s]")

    # Phase 2: Linear descent for exact proof
    print(f"    Phase 2: Linear descent [{lo}..{hi}]")
    for k in range(hi, lo - 1, -1):
        t1 = time.time()
        result = _try_solve(grid_rows, grid_cols, k, placements, piece_aux,
                            n_pieces, var, cell_vars, shape_clauses, aux_id)
        elapsed = time.time() - t1

        if result is not None:
            best_size = k
            best_cells = result
            print(f"    k = {k}: SAT  [{elapsed:.1f}s]")
        else:
            print(f"    k = {k}: UNSAT  [{elapsed:.1f}s]  "
                  "<-- optimality proved")
            break

    return best_size, best_cells


def _try_solve(grid_rows, grid_cols, target_cells, placements, piece_aux,
               n_pieces, var, cell_vars, shape_clauses, top_id):
    """Try to find a connected polyiamond of exactly target_cells cells."""
    solver = _SAT_SOLVER()

    # Placement constraints: each piece must be placed at least once
    for i in range(n_pieces):
        solver.add_clause(piece_aux[i])
    for i, pplacements in enumerate(placements):
        for j, placed_cells in enumerate(pplacements):
            aux = piece_aux[i][j]
            for r, c in placed_cells:
                solver.add_clause([-aux, var(r, c)])

    # Shape constraints (empty list if unconstrained)
    for clause in shape_clauses:
        solver.add_clause(clause)

    # Min-degree constraint: every occupied cell has >= 1 occupied neighbor.
    # Eliminates isolated cells, reducing disconnected solutions for CEGAR.
    for r in range(grid_rows):
        for c in range(grid_cols):
            nbs = []
            if c > 0:
                nbs.append((r, c - 1))
            if c < grid_cols - 1:
                nbs.append((r, c + 1))
            if (r + c) % 2 == 0:
                if r > 0:
                    nbs.append((r - 1, c))
            else:
                if r < grid_rows - 1:
                    nbs.append((r + 1, c))
            if nbs:
                solver.add_clause([-var(r, c)] +
                                  [var(nr, nc) for nr, nc in nbs])

    # Cardinality: exactly target_cells occupied
    card_clauses = CardEnc.equals(cell_vars, target_cells, top_id=top_id,
                                  encoding=EncType.totalizer)
    for clause in card_clauses:
        solver.add_clause(clause)

    # Iterative connectivity refinement (CEGAR)
    for iteration in range(200):
        if not solver.solve():
            solver.delete()
            return None

        model = set(solver.get_model())
        occupied = set()
        for r in range(grid_rows):
            for c in range(grid_cols):
                if var(r, c) in model:
                    occupied.add((r, c))

        if len(occupied) != target_cells:
            solver.delete()
            return None

        components = find_components(occupied)
        if len(components) == 1:
            solver.delete()
            return occupied

        # Add cuts to eliminate disconnected solutions
        largest = max(components, key=len)
        for comp in components:
            if comp is not largest:
                solver.add_clause([-var(r, c) for r, c in comp])

    solver.delete()
    return None


