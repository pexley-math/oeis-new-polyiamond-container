def parse_n_arg(s):
    """Parse --n argument: '5', '3-7', '0,3,5', '3-5,9'."""
    values = []
    for part in s.split(","):
        part = part.strip()
        if "-" in part:
            lo, hi = part.split("-", 1)
            values.extend(range(int(lo), int(hi) + 1))
        else:
            values.append(int(part))
    return sorted(set(values))


