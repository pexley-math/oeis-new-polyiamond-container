"""
Smallest Polyiamond Containing All Free n-Iamonds

Computes the smallest connected polyiamond that contains every free n-iamond
as a subregion (i.e., every free n-iamond can be placed somewhere within the
polyiamond via translation, rotation, and reflection).

This is the triangular grid analog of A327094 (square grid) and the polyhex
container sequence (hex grid).

Method: SAT solver (Glucose 4.2 via PySAT) with iterative connectivity
cuts and top-down search: start at an upper bound and decrease k until
UNSAT, proving the exact minimum.

Triangular grid: cell (r,c) is up-pointing if (r+c) is even, down-pointing
if (r+c) is odd. Each cell has exactly 3 edge-neighbors.

Requirements: Python 3.8+, python-sat
Install:      pip install python-sat

Usage:
    python solve_polyiamond_container.py                  # Run all n=1..8
    python solve_polyiamond_container.py --n 5            # Run single n
    python solve_polyiamond_container.py --n 3-6          # Run range
    python solve_polyiamond_container.py --verbose        # Detailed output
    python solve_polyiamond_container.py --log run.txt    # Log output to file
    python solve_polyiamond_container.py --json results.json  # Save as JSON

Author: Peter Exley
License: CC-BY-4.0
"""

