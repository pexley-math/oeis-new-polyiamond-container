def format_tri_grid(cells):
    """Format a polyiamond as ASCII art using ^/v for up/down triangles."""
    if not cells:
        return "    (empty)"
    cs = set(cells)
    r0 = min(r for r, c in cs)
    r1 = max(r for r, c in cs)
    c0 = min(c for r, c in cs)
    c1 = max(c for r, c in cs)
    lines = []
    for r in range(r0, r1 + 1):
        line = ""
        for c in range(c0, c1 + 1):
            if (r, c) in cs:
                if (r + c) % 2 == 0:
                    line += " ^"
                else:
                    line += " v"
            else:
                line += " ."
        lines.append("    " + line)
    return "\n".join(lines)


